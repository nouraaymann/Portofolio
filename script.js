  function showPage(name) {
    document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
    document.querySelectorAll('.nav-links a').forEach(a => a.classList.remove('active'));
    document.getElementById('page-' + name).classList.add('active');
    const navEl = document.getElementById('nav-' + name);
    if (navEl) navEl.classList.add('active');
    window.scrollTo(0, 0);
  }

  function showProject(key) {
    const p = projects[key];
    if (!p) return;

const heroImg = document.getElementById("project-hero-img");

if (p.cover) {
    heroImg.src = p.cover;
    heroImg.alt = p.title;
    heroImg.style.display = "block";
} else {
    heroImg.style.display = "none";
}    document.getElementById('project-title').textContent = p.title;
    document.getElementById('project-type').textContent = p.type;
    document.getElementById('project-role').textContent = p.role;
    document.getElementById('project-date').textContent = p.date;
    const linkEl = document.getElementById('project-link');
    linkEl.textContent = p.link.label;
    linkEl.href = p.link.url;
    document.getElementById('project-desc').textContent = p.desc;

    const gallery = document.getElementById('project-gallery');
    gallery.innerHTML = '';
    if (p.galleryHTML) {
      gallery.innerHTML = p.galleryHTML;
    } else if (p.gallery) {
      p.gallery.forEach(row => {
        const rowEl = document.createElement('div');
        rowEl.className = `gallery-row cols-${row.cols}`;
        row.items.forEach(item => {
          const imgEl = document.createElement('div');
          imgEl.className = 'gallery-img';
          imgEl.innerHTML = `<div class="gallery-placeholder">${item.label}<br><small style="color:#ccc;margin-top:6px;display:block">Add your screenshot here</small></div>`;
          rowEl.appendChild(imgEl);
        });
        gallery.appendChild(rowEl);
      });
    }

    document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
    document.querySelectorAll('.nav-links a').forEach(a => a.classList.remove('active'));
    document.getElementById('page-project').classList.add('active');
    document.getElementById('nav-portfolio').classList.add('active');
    window.scrollTo(0, 0);
  }
